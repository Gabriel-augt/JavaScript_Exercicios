// Soma, subtração, multiplicação e divisão dos valores.
function operacoes (n1, n2) {
    const soma = n1 + n2
    const subtracao = n1 - n2
    const multiplicacao = n1 * n2
    const divisao = n1 / n2
    console.log(`Soma: ${n1} + ${n2} = ${soma}.`)
    console.log(`Subtração: ${n1} - ${n2} = ${subtracao}.`)
    console.log(`Multiplicação: ${n1} x ${n2} = ${multiplicacao}.`)
    console.log(`Divisão: ${n1} / ${n2} = ${divisao}.`)

}

operacoes(7,2)

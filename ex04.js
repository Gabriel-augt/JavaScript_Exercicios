// Divisão e resto da divisão.
function divisao (dividendo , divisor) {
    let dividir = dividendo / divisor
    let restoDivisao = dividendo % divisor
    console.log(`A divisão entre ${dividendo} e ${divisor} é ${dividir}.`)
    console.log(`O resto da divisão entre ${dividendo} e ${divisor} é ${restoDivisao}.`)
}
divisao(11,4)
